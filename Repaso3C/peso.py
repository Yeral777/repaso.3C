def kilogramos_gramos(gramos):
    formula = gramos * 1000
    return f"El valor es {formula} gramos"

def kilogramos_libras(libras):
    formula = libras * 2.20462
    return f"El valor es {formula} libras"

def kilogramo_onzas(onzas):
    formula = onzas * 35.274
    return f"El valor es {formula} onzas"